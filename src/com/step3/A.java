package com.step3;

public class A {

	public static void main(String[] args) {
		//%연산자는 어떤 수를 2로 나눈 나머지값을 반환하는 연산자 입니다.
		if(5%2==1) {
			System.out.println("5는 홀수 입니다.");
		}
		if(4%2==0) {
			System.out.println("4는 짝수 입니다.");
		}
		if(1000%2==0) {
			System.out.println("1000는 짝수 입니다.");
		}

	}

}
